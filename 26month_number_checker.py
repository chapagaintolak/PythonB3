# Create Program that display day of week Sunday to Saturday [1-7] 
# Use elif 
month_num = int(input("Enter month number: "))

if month_num == 1:
    print("Month is January")
elif month_num == 2:
    print("Month is February")
elif month_num == 3:
    print("Month is March")
else:
    print("Invalid month number")